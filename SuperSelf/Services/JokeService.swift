import Foundation
import Combine

@MainActor
class JokeService: ObservableObject {
    static let shared = JokeService()

    @Published var currentJoke: String = "加载中..."
    @Published var isLoading: Bool = false

    private let localJokes = JokeLibrary.jokes
    private var remainingJokes: [String] = []

    init() {
        Task {
            await fetchJoke()
        }
    }

    func fetchJoke() async {
        isLoading = false
        showNextJoke()
    }

    private func showNextJoke() {
        if remainingJokes.isEmpty {
            refillJokes()
        }

        currentJoke = remainingJokes.popLast() ?? "笑话库还是空的，等你添加新笑话。"
    }

    private func refillJokes() {
        remainingJokes = localJokes.shuffled()

        if remainingJokes.last == currentJoke, remainingJokes.count > 1 {
            remainingJokes.swapAt(0, remainingJokes.count - 1)
        }
    }
}

